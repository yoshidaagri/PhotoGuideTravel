/**
 * Check authentication status on page load
 * Ë™çË®ºÁä∂ÊÖãÁ¢∫Ë™çÔºà„Éö„Éº„Ç∏„É≠„Éº„ÉâÊôÇÔºâ
 */
async function checkAuthenticationStatus() {
    console.log('üîê Checking authentication status...');
    
    try {
        // Get stored authentication data
        const tourismAuth = localStorage.getItem('tourismAuth');
        const accessToken = localStorage.getItem('accessToken');
        const userInfo = localStorage.getItem('userInfo');
        
        if (!tourismAuth || !accessToken) {
            console.log('‚ùå No authentication data found');
            return false;
        }
        
        console.log('üìä Found stored auth data, verifying with server...');
        
        // Check for emergency login token
        if (accessToken === 'emergency-login-token-dev') {
            console.log('üõ†Ô∏è Emergency login detected, bypassing server verification');
            
            // Use stored user info for emergency login
            if (userInfo) {
                const storedUserInfo = JSON.parse(userInfo);
                currentUser = storedUserInfo;
                isAuthenticated = true;
                console.log('‚úÖ Emergency authentication successful:', storedUserInfo.email);
                return true;
            } else {
                // Fallback emergency user
                currentUser = {
                    email: 'emergency@example.com',
                    name: 'Emergency User',
                    auth_provider: 'emergency'
                };
                isAuthenticated = true;
                console.log('‚úÖ Emergency authentication successful (fallback)');
                return true;
            }
        }
        
        // Verify token with backend for real tokens
        const response = await fetch('https://f4n095fm2j.execute-api.ap-northeast-1.amazonaws.com/dev/auth/user-info', {
            method: 'GET',
            headers: {
                'Authorization': `Bearer ${accessToken}`,
                'Content-Type': 'application/json'
            }
        });
        
        if (response.ok) {
            const serverUserInfo = await response.json();
            console.log('‚úÖ Authentication verified:', serverUserInfo.email);
            
            // Set global authentication state
            currentUser = serverUserInfo;
            isAuthenticated = true;
            
            // Update stored user info if needed
            if (userInfo) {
                const storedUserInfo = JSON.parse(userInfo);
                if (storedUserInfo.email === serverUserInfo.email) {
                    console.log('üìß Stored user info matches server data');
                } else {
                    console.log('üîÑ Updating stored user info');
                    localStorage.setItem('userInfo', JSON.stringify(serverUserInfo));
                }
            }
            
            return true;
            
        } else {
            console.log('‚ö†Ô∏è Token validation failed:', response.status);
            await clearAuthenticationData();
            return false;
        }
        
    } catch (error) {
        console.error('üö® Authentication check error:', error);
        await clearAuthenticationData();
        return false;
    }
}

/**
 * Clear all authentication data
 * Ë™çË®º„Éá„Éº„Çø„ÇØ„É™„Ç¢
 */
async function clearAuthenticationData() {
    console.log('üßπ Clearing authentication data...');
    
    // Clear localStorage
    localStorage.removeItem('tourismAuth');
    localStorage.removeItem('accessToken');
    localStorage.removeItem('userInfo');
    
    // Clear sessionStorage (for backward compatibility)
    sessionStorage.removeItem('tourismAuth');
    sessionStorage.removeItem('accessToken');
    sessionStorage.removeItem('userInfo');
    sessionStorage.removeItem('tourismUser');
    
    // Clear any cached user data
    localStorage.removeItem('selectedLanguage');
    sessionStorage.removeItem('selectedLanguage');
    
    // Reset global state
    currentUser = null;
    isAuthenticated = false;
    
    console.log('‚úÖ Authentication data cleared completely');
}

